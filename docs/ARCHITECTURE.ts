/**
 * 智能数据可视化架构 - 数据流向图
 *
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                         用户输入层                                    │
 * │  "帮我画一个柱状图，展示苹果、香蕉、橘子的销量"                        │
 * └──────────────────────────────┬──────────────────────────────────────┘
 *                                 │
 *                                 ▼
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                      AI 推理层 (LLM)                                 │
 * │  - 分析用户意图                                                       │
 * │  - 识别需要调用 generateChart 工具                                   │
 * │  - 生成 ECharts option JSON                                          │
 * └──────────────────────────────┬──────────────────────────────────────┘
 *                                 │
 *                                 ▼
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                    Tool 执行层                                       │
 * │  generateChart({                                                     │
 * │    rawOptionString: "{title: {...}, series: [...]}"                │
 * │    agentTrace: "ChartRenderer"                                      │
 * │  })                                                                  │
 * └──────────────────────────────┬──────────────────────────────────────┘
 *                                 │
 *                                 ▼
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                    前端渲染层                                         │
 * │  ┌─────────────────────────────────────────────────────────────┐   │
 * │  │  ChartRenderer 组件                                           │   │
 * │  │  1. 接收 rawOptionString                                      │   │
 * │  │  2. 初始化 Web Worker                                         │   │
 * │  │  3. 发送数据到 Worker                                         │   │
 * │  └───────────────────┬───────────────────────────────────────────┘   │
 * └────────────────────────┼────────────────────────────────────────────┘
 *                          │
 *                          ▼
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │              Web Worker 容错层 (json-worker.js)                      │
 * │  ┌──────────────────────────────────────────────────────────────┐   │
 * │  │ 步骤 1: 清除 markdown 标记 (```json, ```)                     │   │
 * │  │ 步骤 2: 尝试直接 JSON.parse()                                  │   │
 * │  │ 步骤 3: 失败则调用 jsonrepair() 修复                          │   │
 * │  │ 步骤 4: 验证并返回结果                                         │   │
 * │  └──────────────────┬───────────────────────────────────────────┘   │
 * └────────────────────────┼────────────────────────────────────────────┘
 *                          │
 *                  ┌───────┴───────┐
 *                  │               │
 *             Success           Failure
 *                  │               │
 *                  ▼               ▼
 *      ┌──────────────────┐  ┌──────────────────┐
 *      │  返回解析后的     │  │  返回错误信息     │
 *      │  ECharts option  │  │  触发容错 UI      │
 *      └────────┬─────────┘  └────────┬─────────┘
 *               │                     │
 *               │                     ▼
 *               │          ┌──────────────────────┐
 *               │          │  Error Boundary       │
 *               │          │  - 显示错误提示       │
 *               │          │  - 提示重新生成       │
 *               │          └──────────────────────┘
 *               │
 *               ▼
 * ┌─────────────────────────────────────────────────────────────────────┐
 * │                    图表渲染层 (ReactECharts)                         │
 * │  ┌──────────────────────────────────────────────────────────────┐   │
 * │  │  - 渲染交互式图表                                              │   │
 * │  │  - 支持缩放、提示框等交互                                      │   │
 * │  │  - 响应式布局                                                  │   │
 * │  │  - 主题自适应 (亮色/暗色)                                     │   │
 * │  └──────────────────────────────────────────────────────────────┘   │
 * └─────────────────────────────────────────────────────────────────────┘
 *
 * 关键设计特点：
 * ═══════════════════════════════════════════════════════════════════════
 *
 * 1. 异步非阻塞
 *    - Web Worker 在后台线程运行，不阻塞 UI
 *    - 主线程保持响应，用户体验流畅
 *
 * 2. 多层容错
 *    - Layer 1: jsonrepair 自动修复 JSON
 *    - Layer 2: 主线程降级解析
 *    - Layer 3: Error Boundary 兜底 UI
 *
 * 3. 流式体验
 *    - 显示 "AI Agent [ChartRenderer] 正在处理数据..."
 *    - 渐进式渲染，避免白屏
 *
 * 4. 类型安全
 *    - TypeScript 完整类型推导
 *    - InferUITool 自动推导工具类型
 *
 * 5. 可扩展性
 *    - 工具层可接入真实后端 API
 *    - 支持多智能体编排 (DataAnalyzer + ChartRenderer + ...)
 */

